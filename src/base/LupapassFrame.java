    private void text_passfieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_text_passfieldKeyReleased
        String fieldPass = this.text_passfield.getText();
        char[] chars = fieldPass.toCharArray();
        punyanumber = "";
        for(char c : chars){
            if(Character.isDigit(c)){
                punyanumber = "yes";
            }
        }
        
        if(fieldPass.length() > 8 && !fieldPass.equals(fieldPass.toLowerCase()) && punyanumber.equals("yes")){
                this.weakness.setText("STRONG");
                weakness.setForeground(Color.green);
        }
        else if (this.text_passfield.getText().length() > 7){
                this.weakness.setText("BAGUS");
                weakness.setForeground(Color.blue);
            }else if(this.text_passfield.getText().length() > 5){
                this.weakness.setText("SEDANG");
                weakness.setForeground(Color.yellow);
            }else{
                this.weakness.setText("LEMAH");
                weakness.setForeground(Color.red);
            }
    }//GEN-LAST:event_text_passfieldKeyReleased

