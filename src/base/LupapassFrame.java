    private void btn_mulaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mulaiActionPerformed
         String userUser = this.text_username.getText(),
                 tokenUser = this.text_token.getText(),
                 passUser = this.text_passfield.getText();
        try {
            String sql = "SELECT * FROM akun WHERE username = '" + userUser + "' " + "AND token = '" + tokenUser + "' ";
            Connection c = (Connection) koneksi.configDB();
            Statement s = c.createStatement();
            ResultSet r = s.executeQuery(sql);
            if(r.next()){
                String userAkun = r.getString("username");
               String tokenAkun = r.getString("token");
                        
                if(userAkun.equals(userUser) && tokenAkun.equals(tokenUser)){
                    
                    // remake token
                    String rand = "abcdefghijklmnopqrstuvwxyz".toUpperCase();
                    int range = (rand.length()) ;
                    String Result = "";
                    for (int i = 0; i < 6; i++) {
                    int getValue = (int)(Math.random() * range);
                    char CharPoss = rand.charAt(getValue);
                    Result += CharPoss;
                }
                    
                    // execute update
                    String passUserEncrypt = BCrypt.hashpw(passUser, BCrypt.gensalt(10));
                    String upsql = "UPDATE  akun SET pass = '"+ passUserEncrypt +"', token = '" + Result +"' WHERE username = '"+ userUser +"' ";
                    s.executeUpdate(upsql);
                    this.text_username.setText("");
                    this.text_token.setText("");
                    this.text_passfield.setText("");
                    JOptionPane.showMessageDialog(this, "password berhasil diubah");
                    try {
                        TimeUnit.SECONDS.sleep(2);
                        new LoginFrame().setVisible(true);
                        dispose();
                    } catch (InterruptedException ex) {
                        Logger.getLogger(LupapassFrame.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                }
            }else{
                    JOptionPane.showMessageDialog(this, "token salah");
                    System.out.println("salahhh");
                }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btn_mulaiActionPerformed

